package com.xiaonei.domain;

public class UserSenior {
	private Integer id;
	private Users users;
	private Senior senior;
	private Integer seniorYear;
	private String grade1;
	private String grade2;
	private String grade3;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Users getUsers() {
		return users;
	}
	public void setUsers(Users users) {
		this.users = users;
	}
	public Senior getSenior() {
		return senior;
	}
	public void setSenior(Senior senior) {
		this.senior = senior;
	}
	public Integer getSeniorYear() {
		return seniorYear;
	}
	public void setSeniorYear(Integer seniorYear) {
		this.seniorYear = seniorYear;
	}
	public String getGrade1() {
		return grade1;
	}
	public void setGrade1(String grade1) {
		this.grade1 = grade1;
	}
	public String getGrade2() {
		return grade2;
	}
	public void setGrade2(String grade2) {
		this.grade2 = grade2;
	}
	public String getGrade3() {
		return grade3;
	}
	public void setGrade3(String grade3) {
		this.grade3 = grade3;
	}
	
	

}
